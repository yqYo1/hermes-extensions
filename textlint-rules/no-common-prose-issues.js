module.exports = function (context) {
    const { Syntax, RuleError, report, getSource } = context;

    return {
        [Syntax.Str](node) {
            const text = getSource(node);

            if (!text) return;

            // Check for common misspellings (whole-word matches only)
            const words = text.match(/\b[a-zA-Z']+\b/g) || [];
            for (const word of words) {
                const lower = word.toLowerCase();
                if (MISSPELLINGS[lower]) {
                    report(
                        node,
                        new RuleError(
                            `Possible misspelling: "${word}" should be "${MISSPELLINGS[lower]}"`,
                        ),
                    );
                }
            }

            // Check for "could of", "should of", etc. (two-word patterns)
            if (/\b(could|should|would|might|must) of\b/i.test(text)) {
                const match = text.match(/\b(could|should|would|might|must) of\b/i);
                if (match) {
                    report(
                        node,
                        new RuleError(
                            `"${match[0]}" should be "${match[1].toLowerCase()} have"`,
                        ),
                    );
                }
            }
        },
    };
};
